CREATE TABLE USUARIOS (
    PKUSUARIO SERIAL PRIMARY KEY,
    CODUSUARIO VARCHAR(12) NOT NULL UNIQUE,
    NOMBRES VARCHAR(80) NOT NULL,
    APELLIDOS VARCHAR(80) NOT NULL,
    CORREO VARCHAR(120) NOT NULL UNIQUE,
    PASSWORD_HASH VARCHAR(255) NOT NULL,
    TELEFONO VARCHAR(20),
    PKROL INT NOT NULL REFERENCES ROLES(PKROL),
    FECHAREGISTRO TIMESTAMP NOT NULL DEFAULT NOW(),
    ULTIMO_ACCESO TIMESTAMP,
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE ESTUDIANTES (
    PKESTUDIANTE SERIAL PRIMARY KEY,
    CODESTUDIANTE VARCHAR(12) NOT NULL UNIQUE,
    PKUSUARIO INT NOT NULL UNIQUE REFERENCES USUARIOS(PKUSUARIO) ON DELETE CASCADE,
    PKGRADO INT NOT NULL REFERENCES GRADOS_ACADEMICOS(PKGRADO),
    COLEGIO VARCHAR(120) NOT NULL,
    PKDISTRITO INT NOT NULL REFERENCES DISTRITOS(PKDISTRITO),
    PROMEDIOACTUAL NUMERIC(5,2) DEFAULT 0,
    HORASESTUDIADAS NUMERIC(6,2) DEFAULT 0,
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE PADRES (
    PKPADRE SERIAL PRIMARY KEY,
    CODPADRE VARCHAR(12) NOT NULL UNIQUE,
    PKUSUARIO INT NOT NULL UNIQUE REFERENCES USUARIOS(PKUSUARIO) ON DELETE CASCADE,
    OCUPACION VARCHAR(100),
    DIRECCION VARCHAR(150),
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE PADRES_ESTUDIANTES (
    PKPADRE INT NOT NULL REFERENCES PADRES(PKPADRE) ON DELETE CASCADE,
    PKESTUDIANTE INT NOT NULL REFERENCES ESTUDIANTES(PKESTUDIANTE) ON DELETE CASCADE,
    PKPARENTESCO INT NOT NULL REFERENCES PARENTESCOS(PKPARENTESCO),
    RESPONSABLE_PRINCIPAL CHAR(1) NOT NULL DEFAULT 'S',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW(),
    PRIMARY KEY (PKPADRE, PKESTUDIANTE)
);

CREATE TABLE DOCENTES (
    PKDOCENTE SERIAL PRIMARY KEY,
    CODDOCENTE VARCHAR(12) NOT NULL UNIQUE,
    PKUSUARIO INT NOT NULL UNIQUE REFERENCES USUARIOS(PKUSUARIO) ON DELETE CASCADE,
    ESPECIALIDAD VARCHAR(100) NOT NULL,
    EXPERIENCIA VARCHAR(150),
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE SESIONES_USUARIO (
    PKSESION BIGSERIAL PRIMARY KEY,
    PKUSUARIO INT NOT NULL REFERENCES USUARIOS(PKUSUARIO) ON DELETE CASCADE,
    TOKEN_REFRESH VARCHAR(500),
