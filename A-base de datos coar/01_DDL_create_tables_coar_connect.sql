    ENUNCIADO TEXT NOT NULL,
    PUNTAJE NUMERIC(5,2) NOT NULL DEFAULT 1 CHECK (PUNTAJE > 0),
    ORDEN INT NOT NULL DEFAULT 1,
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
