-- ============================================================
-- BLOQUE 3: CURSOS, HORARIOS E INSCRIPCIONES
-- ============================================================

CREATE TABLE CURSOS (
    PKCURSO SERIAL PRIMARY KEY,
    CODCURSO VARCHAR(10) NOT NULL UNIQUE,
    NOMCURSO VARCHAR(100) NOT NULL,
    DESCURSO TEXT,
    PKAREAACADEMICA INT NOT NULL REFERENCES AREAS_ACADEMICAS(PKAREAACADEMICA),
    PKNIVELCURSO INT NOT NULL REFERENCES NIVELES_CURSO(PKNIVELCURSO),
    PKDOCENTE INT REFERENCES DOCENTES(PKDOCENTE) ON DELETE SET NULL,
    IMAGEN_URL TEXT,
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE HORARIOS (
    PKHORARIO SERIAL PRIMARY KEY,
    CODHORARIO VARCHAR(12) NOT NULL UNIQUE,
    PKCURSO INT NOT NULL REFERENCES CURSOS(PKCURSO) ON DELETE CASCADE,
    DIA_SEMANA VARCHAR(20) NOT NULL,
    HORA_INICIO TIME NOT NULL,
    HORA_FIN TIME NOT NULL,
    PKMODALIDADCLASE INT NOT NULL REFERENCES MODALIDADES_CLASE(PKMODALIDADCLASE),
    CUPOS INT NOT NULL DEFAULT 0 CHECK (CUPOS >= 0),
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW(),
    CHECK (HORA_FIN > HORA_INICIO)
);

CREATE TABLE INSCRIPCIONES (
    PKINSCRIPCION SERIAL PRIMARY KEY,
    CODINSCRIPCION VARCHAR(12) NOT NULL UNIQUE,
    PKESTUDIANTE INT NOT NULL REFERENCES ESTUDIANTES(PKESTUDIANTE) ON DELETE CASCADE,
    PKHORARIO INT NOT NULL REFERENCES HORARIOS(PKHORARIO) ON DELETE CASCADE,
    PKESTADOINSCRIPCION INT NOT NULL REFERENCES ESTADOS_INSCRIPCION(PKESTADOINSCRIPCION),
    FECHAINSCRIPCION TIMESTAMP NOT NULL DEFAULT NOW(),
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE (PKESTUDIANTE, PKHORARIO)
);

