CREATE TABLE SIMULACROS (
    PKSIMULACRO SERIAL PRIMARY KEY,
    CODSIMULACRO VARCHAR(12) NOT NULL UNIQUE,
    TITULO VARCHAR(150) NOT NULL,
    DESCRIPCION TEXT,
    PKCURSO INT REFERENCES CURSOS(PKCURSO) ON DELETE SET NULL,
    PKTIPOSIMULACRO INT NOT NULL REFERENCES TIPOS_SIMULACRO(PKTIPOSIMULACRO),
    TIEMPO_MINUTOS INT NOT NULL CHECK (TIEMPO_MINUTOS > 0),
    FECHAPUBLICACION DATE NOT NULL DEFAULT CURRENT_DATE,
    ESTADO CHAR(1) NOT NULL DEFAULT '1',
    FECULTACTUALIZACION TIMESTAMP NOT NULL DEFAULT NOW()
