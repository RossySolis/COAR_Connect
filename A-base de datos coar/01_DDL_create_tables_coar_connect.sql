    PKALTERNATIVA INT REFERENCES ALTERNATIVAS(PKALTERNATIVA) ON DELETE SET NULL,
    ES_CORRECTA CHAR(1) NOT NULL DEFAULT 'N',
    PUNTAJE_OBTENIDO NUMERIC(5,2) NOT NULL DEFAULT 0,
    FECHARESPUESTA TIMESTAMP NOT NULL DEFAULT NOW(),
