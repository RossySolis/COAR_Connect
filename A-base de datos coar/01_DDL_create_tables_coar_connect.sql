CREATE TABLE RESULTADOS_SIMULACRO (
    PKRESULTADOSIMULACRO SERIAL PRIMARY KEY,
    PKRESULTADO INT NOT NULL REFERENCES RESULTADOS(PKRESULTADO) ON DELETE CASCADE,
