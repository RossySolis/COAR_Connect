CREATE TABLE RESULTADOS (
    PKRESULTADO SERIAL PRIMARY KEY,
    CODRESULTADO VARCHAR(12) NOT NULL UNIQUE,
    PKESTUDIANTE INT NOT NULL REFERENCES ESTUDIANTES(PKESTUDIANTE) ON DELETE CASCADE,
    PKSIMULACRO INT NOT NULL REFERENCES SIMULACROS(PKSIMULACRO) ON DELETE CASCADE,
    PKESTADOEVALUACION INT NOT NULL REFERENCES ESTADOS_EVALUACION(PKESTADOEVALUACION),
    PUNTAJE NUMERIC(6,2) NOT NULL DEFAULT 0 CHECK (PUNTAJE >= 0),
    NOTA_VIGESIMAL NUMERIC(5,2) DEFAULT 0 CHECK (NOTA_VIGESIMAL >= 0),
    FECHAINICIO TIMESTAMP,
    FECHAFIN TIMESTAMP,
    OBSERVACION TEXT,
