CREATE TABLE ALTERNATIVAS (
    PKALTERNATIVA SERIAL PRIMARY KEY,
    PKPREGUNTA INT NOT NULL REFERENCES PREGUNTAS(PKPREGUNTA) ON DELETE CASCADE,
